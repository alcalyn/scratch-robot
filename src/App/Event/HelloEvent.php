<?php

namespace App\Event;

use Symfony\Component\EventDispatcher\Event;

class HelloEvent extends Event
{
    /**
     * @var string
     */
    const HELLO = 'event.hello';

    /**
     * @var string
     */
    private $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return self
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }
}
